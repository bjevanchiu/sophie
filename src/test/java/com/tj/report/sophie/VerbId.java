/**
 * 标识消息Id.
 */
public class VerbId {
    /**
     * 标识消息Id的类型.
     */
    private String category;

    /**
     * 表示消息Id的Id.
     */
    private String id;

    public String getCategory() {
        return category;
    }

    public void setCategory(String category) {
        this.category = category;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }
}
